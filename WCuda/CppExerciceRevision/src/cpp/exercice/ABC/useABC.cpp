    ABCp abc(1, 2, 3);
    cout << abc << endl;
    Quadratique quadratique(abc);

    int nbSolutions = quadratique.solve();
    cout << "Nb solutions: " << nbSolutions << endl;
    double* tabX = quadratique.getTabX();
    for (int i=0; i< nbSolutions; i++)
	{
	cout << tabX[i] << endl;
	}
